def compute_flops(n_params: float, n_tokens: float, multiplier: float = 6.0) -> float:
    """
    Calculate total training FLOPs for a given model size and token count.

    Formula: FLOPs = multiplier * N * D

    The default multiplier is 6.0, which accounts for:
        - Forward pass: 2 * N * D FLOPs
        - Backward pass: 4 * N * D FLOPs (gradients + activation gradients)
        - Total: 6 * N * D FLOPs

    Args:
        n_params: Number of model parameters (N)
        n_tokens: Number of training tokens (D)
        multiplier: FLOPs multiplier (default 6.0)

    Returns:
        Total training FLOPs (C)

    Example:
        >>> compute_flops(1e9, 1e9)  # 1B params, 1B tokens
        6e+18
    """
    return multiplier * n_params * n_tokens


def generate_isoflop_curves(
    config: IsoFLOPsConfig | None = None,
    compute_budgets: list[float] | None = None,
) -> dict[float, list[dict[str, float]]]:
    """
    Generate (N, D) configurations for fixed FLOPs budgets.

    For each compute budget C, generates multiple (N, D) pairs that satisfy:
        C = 6 * N * D

    The configurations are generated by sweeping N (parameters) in log space
    and computing D = C / (6 * N).

    Args:
        config: IsoFLOPsConfig instance (uses defaults if None)
        compute_budgets: Override list of compute budgets (uses config.compute_budgets if None)

    Returns:
        Dictionary mapping compute budget to list of (N, D) configurations:
        {
            1e18: [
                {"n_params": 1e6, "n_tokens": 1.67e11, "flops": 1e18},
                {"n_params": 3.16e6, "n_tokens": 5.27e10, "flops": 1e18},
                ...
            ],
            1e19: [...],
            ...
        }

    Example:
        >>> config = IsoFLOPsConfig(compute_budgets=[1e18], n_points_per_budget=5)
        >>> curves = generate_isoflop_curves(config)
        >>> len(curves[1e18])
        5
    """
    if config is None:
        config = IsoFLOPsConfig()

    budgets = compute_budgets if compute_budgets is not None else config.compute_budgets
    n_min, n_max = config.n_range
    multiplier = config.flops_per_token_per_param

    curves: dict[float, list[dict[str, float]]] = {}

    for C in budgets:
        # For this compute budget, generate N values in log space
        # such that D = C / (6 * N) stays within reasonable bounds

        # Compute valid N range for this C
        # D must be at least 1 token, so N <= C / 6
        # D should be at most C / (6 * n_min) to avoid tiny N
        n_upper = min(n_max, C / (multiplier * 1.0))  # At least 1 token
        n_lower = max(n_min, C / (multiplier * 1e13))  # At most 1e13 tokens (generous upper bound)

        # Generate N values in log space
        log_n_values = np.linspace(np.log10(n_lower), np.log10(n_upper), config.n_points_per_budget)
        n_values = 10 ** log_n_values

        points = []
        for N in n_values:
            D = C / (multiplier * N)
            points.append({
                "n_params": float(N),
                "n_tokens": float(D),
                "flops": float(C),
            })

        curves[float(C)] = points

    return curves


def find_optimal_nd_for_compute(
    compute_budget: float,
    loss_data: list[dict[str, float]],
    multiplier: float = 6.0,
) -> dict[str, float] | None:
    """
    Find the optimal (N, D) configuration for a given compute budget.

    Given experimental data with different (N, D) pairs and their losses,
    finds the configuration with the minimum loss that matches the compute budget.

    Args:
        compute_budget: Target compute budget in FLOPs (C)
        loss_data: List of experimental results, each containing:
            - n_params: Model parameters
            - n_tokens: Training tokens
            - loss: Validation loss
        multiplier: FLOPs multiplier (default 6.0)

    Returns:
        Dictionary with optimal configuration, or None if no matching data:
        {
            "n_params": float,
            "n_tokens": float,
            "loss": float,
            "compute": float,
        }
    """
    # Group by compute budget (with tolerance for floating point)
    matching_points = []
    for point in loss_data:
        n_params = point["n_params"]
        n_tokens = point["n_tokens"]
        point_compute = compute_flops(n_params, n_tokens, multiplier)

        # Check if this point matches the target compute budget
        # Use relative tolerance of 10% for compute budget matching
        if abs(point_compute - compute_budget) / compute_budget < 0.1:
            matching_points.append({
                "n_params": n_params,
                "n_tokens": n_tokens,
                "loss": point["loss"],
                "compute": point_compute,
            })

    if not matching_points:
        return None

    # Return the point with minimum loss
    return min(matching_points, key=lambda x: x["loss"])


def fit_power_law(
    compute_values: np.ndarray,
    target_values: np.ndarray,
) -> tuple[float, float]:
    """
    Fit a power law y = A * C^b using log-space linear regression.

    Args:
        compute_values: Array of compute budgets (C)
        target_values: Array of target values (N or D)

    Returns:
        Tuple of (factor_A, exponent_b) where y = A * C^b

    Example:
        >>> C = np.array([1e18, 1e19, 1e20])
        >>> N = np.array([1e9, 3.16e9, 1e10])  # N ∝ C^0.5
        >>> A, b = fit_power_law(C, N)
        >>> print(f"N = {A:.2e} * C^{b:.3f}")
    """
    log_C = np.log(compute_values)
    log_y = np.log(target_values)

    # Linear regression: log(y) = b * log(C) + log(A)
    # Using least squares
    A_matrix = np.vstack([log_C, np.ones(len(log_C))]).T
    b, log_A = np.linalg.lstsq(A_matrix, log_y, rcond=None)[0]

    factor_A = np.exp(log_A)
    exponent_b = b

    return float(factor_A), float(exponent_b)


def predict_optimal_size(
    compute_budget: float,
    factor: float,
    exponent: float,
) -> float:
    """
    Predict optimal model size or token count for a given compute budget.

    Formula: y = factor * C^exponent

    Args:
        compute_budget: Target compute budget (C)
        factor: Power law factor (A)
        exponent: Power law exponent (b)

    Returns:
        Predicted optimal value (N_opt or D_opt)

    Example:
        >>> # Chinchilla: N_opt ≈ 0.1 * C^0.5
        >>> N_opt = predict_optimal_size(1e21, 0.1, 0.5)
        >>> print(f"Optimal params for 1e21 FLOPs: {N_opt:.2e}")
    """
    return factor * (compute_budget ** exponent)


