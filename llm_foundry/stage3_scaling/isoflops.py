"""
llm_foundry/stage3_scaling/isoflops.py — IsoFLOPs Curve Calculator

This module provides utilities for computing IsoFLOPs curves following the
Chinchilla scaling laws (Hoffmann et al., 2022).

Key formulas:
    - FLOPs ≈ 6 * N * D (N=parameters, D=tokens)
    - Chinchilla optimal: N_opt ∝ C^0.5, D_opt ∝ C^0.5

Classes:
    IsoFLOPsConfig: Configuration for IsoFLOPs calculations

Functions:
    compute_flops: Calculate training FLOPs for a given model size and token count
    generate_isoflop_curves: Generate (N, D) configurations for fixed FLOPs budgets
"""
from __future__ import annotations

import json
from dataclasses import dataclass, field
from typing import Any

import numpy as np


# ─────────────────────────────────────────
# Configuration
# ─────────────────────────────────────────

@dataclass
class IsoFLOPsConfig:
    """
    Configuration for IsoFLOPs curve calculations.

    Attributes:
        compute_budgets: List of FLOPs budgets to analyze (e.g., [1e18, 1e19, 1e20])
        n_points_per_budget: Number of (N, D) points to generate per budget
        n_range: Tuple of (min_n, max_n) for parameter sweep in log space
        flops_per_token_per_param: FLOPs multiplier (default 6.0 for forward+backward)
    """
    compute_budgets: list[float] = field(default_factory=lambda: [1e18, 1e19, 1e20, 1e21])
    n_points_per_budget: int = 10
    n_range: tuple[float, float] = (1e6, 1e12)  # (min_params, max_params)
    flops_per_token_per_param: float = 6.0  # 2 for forward + 4 for backward

    def to_dict(self) -> dict[str, Any]:
        """Convert config to dictionary."""
        return {
            "compute_budgets": self.compute_budgets,
            "n_points_per_budget": self.n_points_per_budget,
            "n_range": self.n_range,
            "flops_per_token_per_param": self.flops_per_token_per_param,
        }

    @classmethod
    def from_dict(cls, d: dict[str, Any]) -> IsoFLOPsConfig:
        """Create config from dictionary."""
        return cls(
            compute_budgets=d.get("compute_budgets", [1e18, 1e19, 1e20, 1e21]),
            n_points_per_budget=d.get("n_points_per_budget", 10),
            n_range=tuple(d.get("n_range", [1e6, 1e12])),
            flops_per_token_per_param=d.get("flops_per_token_per_param", 6.0),
        )

    @classmethod
    def from_json(cls, path: str) -> IsoFLOPsConfig:
        """Load config from JSON file."""
        with open(path, "r", encoding="utf-8") as f:
            data = json.load(f)
        return cls.from_dict(data)


