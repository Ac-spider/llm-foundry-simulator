def get_chinchilla_optimal(
    compute_budget: float,
    chinchilla_factor: float = 0.1,  # Approximate from Hoffmann et al.
) -> tuple[float, float]:
    """
    Get Chinchilla-optimal model size and token count for a compute budget.

    Following Hoffmann et al. (2022), the optimal configurations follow:
        N_opt ≈ 0.1 * C^0.5
        D_opt ≈ 10 * C^0.5

    where C is in FLOPs.

    Args:
        compute_budget: Target compute budget in FLOPs
        chinchilla_factor: Scaling factor (default 0.1 for N_opt)

    Returns:
        Tuple of (n_params_optimal, n_tokens_optimal)

    Example:
        >>> N, D = get_chinchilla_optimal(1e21)  # GPT-4 scale
        >>> print(f"Optimal: {N:.2e} params, {D:.2e} tokens")
    """
    C = compute_budget
    N_opt = chinchilla_factor * (C ** 0.5)
    # From C = 6 * N * D, we get D = C / (6 * N)
    D_opt = C / (6.0 * N_opt)
    return N_opt, D_opt


def estimate_training_cost(
    n_params: float,
    n_tokens: float,
    hardware_flops_per_sec: float = 1e12,  # 1 TFLOPS
    hardware_cost_per_hour: float = 2.0,   # $2/hour
) -> dict[str, float]:
    """
    Estimate training time and cost for a given configuration.

    Args:
        n_params: Number of model parameters
        n_tokens: Number of training tokens
        hardware_flops_per_sec: Hardware FLOPs per second
        hardware_cost_per_hour: Hardware cost per hour in dollars

    Returns:
        Dictionary with training estimates:
        {
            "total_flops": float,
            "training_seconds": float,
            "training_hours": float,
            "training_days": float,
            "estimated_cost_usd": float,
        }
    """
    total_flops = compute_flops(n_params, n_tokens)
    training_seconds = total_flops / hardware_flops_per_sec
    training_hours = training_seconds / 3600
    training_days = training_hours / 24
    estimated_cost = training_hours * hardware_cost_per_hour

    return {
        "total_flops": total_flops,
        "training_seconds": training_seconds,
        "training_hours": training_hours,
        "training_days": training_days,
        "estimated_cost_usd": estimated_cost,
    }


def format_flops(flops: float) -> str:
    """
    Format FLOPs value in human-readable form.

    Args:
        flops: FLOPs value

    Returns:
        Human-readable string (e.g., "1.0e18", "1.0 PFLOPs")

    Example:
        >>> format_flops(6e18)
        '6.0e18 (6.0 EFLOPs)'
    """
    units = [
        (1e24, "YFLOPs"),  # Yotta
        (1e21, "ZFLOPs"),  # Zetta
        (1e18, "EFLOPs"),  # Exa
        (1e15, "PFLOPs"),  # Peta
        (1e12, "TFLOPs"),  # Tera
        (1e9, "GFLOPs"),   # Giga
        (1e6, "MFLOPs"),   # Mega
    ]

    for factor, unit in units:
        if flops >= factor:
            return f"{flops:.1e} ({flops/factor:.1f} {unit})"

    return f"{flops:.1e} FLOPs"
